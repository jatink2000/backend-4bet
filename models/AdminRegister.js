const mongoose = require('mongoose');

const adminRegisterSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ['admin'],
    default: 'admin'
  }
}, { timestamps: true });

module.exports = mongoose.model('AdminRegister', adminRegisterSchema);
